CREATE FUNCTION [dbo].[isQualifiedForType]
(
    @MBR_ID int,
	@TYP_ID int
)
RETURNS int
AS
BEGIN
	DECLARE @YES int = (
		SELECT Count(*)
		FROM   QUAL
		WHERE  QUAL.MBR_FK_ID = @MBR_ID AND QUAL.TYP_FK_ID = @TYP_ID
	)

    RETURN CAST(@YES AS BIT) 
	
END
